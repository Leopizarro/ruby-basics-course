# 20.times its like a loop that iterates 20 times


# This code will print o 20 times (with endlines)

20.times { puts "o" }

# This code will print "-" 20 times, but the strings will be concatenated, and there will be no endlines
puts "-" * 20

# 20.to_f will transform 20 to float, and 20.to_s will transform 20 to "20" string

puts 20.to_f
p 20.to_s